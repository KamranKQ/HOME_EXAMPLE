package az.kq.l04.entity;

public class Masin {
	
	private int id;
	private String marka;
	private String model;
	private String reng;
	private int il;
	private String istehsalOlkesi;
	private int istehsalSay;
	private int satisSay;
	
	
	public Masin () {
		
	}


	public int getId() {
		return id;
	}


	public void setId(int id) {
		this.id = id;
	}


	public String getMarka() {
		return marka;
	}


	public void setMarka(String marka) {
		this.marka = marka;
	}


	public String getModel() {
		return model;
	}


	public void setModel(String model) {
		this.model = model;
	}


	public String getReng() {
		return reng;
	}


	public void setReng(String reng) {
		this.reng = reng;
	}


	public int getIl() {
		return il;
	}


	public void setIl(int il) {
		this.il = il;
	}


	public String getIstehsalOlkesi() {
		return istehsalOlkesi;
	}


	public void setIstehsalOlkesi(String istehsalOlkesi) {
		this.istehsalOlkesi = istehsalOlkesi;
	}


	public int getIstehsalSay() {
		return istehsalSay;
	}


	public void setIstehsalSay(int istehsalSay) {
		this.istehsalSay = istehsalSay;
	}


	public int getSatisSay() {
		return satisSay;
	}


	public void setSatisSay(int satisSay) {
		this.satisSay = satisSay;
	}

	
	
}
